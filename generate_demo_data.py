def generate():
    os.makedirs(DATA_DIR, exist_ok=True)

    # ========== 1. 合并总表（4周全部数据）==========
    combined_path = os.path.join(DATA_DIR, "demo_all_weeks_combined.csv")
    with open(combined_path, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.writer(f)
        writer.writerow([
            "伙伴名称", "归属销售",
            "周期起始日期", "周期结束日期",
            "本周新签客户数", "本周回款金额",
            "本周有效商机数", "周活跃度",
            "月度KPI目标新签", "月度KPI目标回款(元)",
            "季度KPI目标新签", "季度KPI目标回款(元)",
            "累计新签", "累计回款(元)",
        ])

        for name, sales, m_new, m_rev, q_new, q_rev in PARTNERS:
            cum_new = 0
            cum_rev = 0
            for wi, (w_start, w_end) in enumerate(WEEKS):
                wk_new, wk_rev, wk_opp, wk_act = WEEKLY_DATA[name][wi]
                cum_new += wk_new
                cum_rev += wk_rev
                writer.writerow([
                    name, sales,
                    w_start, w_end,
                    wk_new, wk_rev,
                    wk_opp, wk_act,
                    m_new, m_rev,
                    q_new, q_rev,
                    cum_new, cum_rev,
                ])

    print(f"[OK] Combined: {combined_path}  ({len(PARTNERS)*4} rows)")

    # ========== 2. 每周独立文件 ==========
    for wi, (w_start, w_end) in enumerate(WEEKS):
        week_label = f"W{wi+1}_{w_start}_to_{w_end}"
        week_path = os.path.join(DATA_DIR, f"demo_{week_label}.csv")

        # 计算累计值（到本周为止）
        with open(week_path, "w", newline="", encoding="utf-8-sig") as f:
            writer = csv.writer(f)
            writer.writerow([
                "伙伴名称", "归属销售",
                "周期起始日期", "周期结束日期",
                "本周新签客户数", "本周回款金额",
                "本周有效商机数", "周活跃度",
                "上周新签客户数", "上周回款金额",
                "上周有效商机数", "上周活跃度",
                "月度KPI目标新签", "月度KPI目标回款(元)",
                "季度KPI目标新签", "季度KPI目标回款(元)",
                "累计新签", "累计回款(元)",
            ])

            for name, sales, m_new, m_rev, q_new, q_rev in PARTNERS:
                wk_new, wk_rev, wk_opp, wk_act = WEEKLY_DATA[name][wi]

                # 上周数据（用于环比）
                if wi > 0:
                    prev_new, prev_rev, prev_opp, prev_act = WEEKLY_DATA[name][wi - 1]
                else:
                    prev_new, prev_rev, prev_opp, prev_act = ("", "", "", "")

                # 累计到本周
                cum_new = sum(WEEKLY_DATA[name][w][0] for w in range(wi + 1))
                cum_rev = sum(WEEKLY_DATA[name][w][1] for w in range(wi + 1))

                writer.writerow([
                    name, sales,
                    w_start, w_end,
                    wk_new, wk_rev, wk_opp, wk_act,
                    prev_new, prev_rev, prev_opp, prev_act,
                    m_new, m_rev, q_new, q_rev,
                    cum_new, cum_rev,
                ])

        print(f"[OK] Week{wi+1}: {week_path}  ({len(PARTNERS)} rows)")

    # ========== 3. 打印趋势速览 ==========
    print("\n" + "=" * 70)
    print(">> 20 Partners x 4 Weeks Trend Overview (New/Rev(k)/Opp)")
    print("=" * 70)

    patterns = {
        "[STABLE]   稳定优秀": ["伙伴A科技公司", "伙伴D网络科技", "伙伴H企业服务", "伙伴L大数据平台", "伙伴N数字营销"],
        "[STUCK]    长期薄弱": ["伙伴B信息技术", "伙伴G人工智能", "伙伴J物联网", "伙伴Q教育科技"],
        "[WORSEN]   持续恶化": ["伙伴E数据服务", "伙伴M移动开发团队", "伙伴P新媒体"],
        "[VOLATILE] 波动不稳定": ["伙伴F云计算", "伙伴I信息安全", "伙伴S电商服务"],
        "[IMPROVE]  改善回暖": ["伙伴K智能硬件", "伙伴O系统集成", "伙伴R金融科技"],
        "[SUDDEN]   突发异常": ["伙伴C软件公司", "伙伴T物流科技"],
    }

    for pattern, names in patterns.items():
        print(f"\n{pattern}:")
        for name in names:
            weeks_str = " -> ".join(
                f"({WEEKLY_DATA[name][w][0]},{WEEKLY_DATA[name][w][1]//1000}k,{WEEKLY_DATA[name][w][2]})"
                for w in range(4)
            )
            print(f"  {name}: {weeks_str}")

    print(f"\n>> All files generated in: {DATA_DIR}")


if __name__ == "__main__":
    generate()
